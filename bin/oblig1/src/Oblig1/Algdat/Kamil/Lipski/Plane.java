package Oblig1.Algdat.Kamil.Lipski;

public class Plane {
	

	private int ankomsttid;
	private int navn;


	public Plane(int navn){
	this.ankomsttid=ankomsttid;
	this.navn=navn;

}



	public int getAnkomsttid() {
		return ankomsttid;
	}



	public void setAnkomsttid(int ankomsttid) {
		this.ankomsttid = ankomsttid;
	}
	
	public int getNavn() {
		return navn;
	}
	public void setNavn(int navn){
		this.navn=navn;
	}
	
	
	
}
